let arreglo = ["Uva","Melón","Manzana","Papaya"];

arreglo.unshift("Sandía"); // Agrega al inicio con Unshift

console.log("Unshift:",arreglo);

arreglo.shift(); // Quito el primer elemento 

console.log("Shift:",arreglo);

arreglo.push("Platano"); // Añadir al final
console.log("Shift:",arreglo);

arreglo.pop() // Quito el elemento final
console.log("Pop:",arreglo);

console.log(arreglo.length); // Tamaño del arreglo (Es una propiedad del objeto arreglo, no un método o una función)



